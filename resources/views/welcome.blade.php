            La aclamada autora argentina Mariana Enríquez ha lanzado su último libro, "Cuentos de la Noche", una colección de relatos que exploran el misterio y el horror en la vida cotidiana. La obra, que ya se ha colocado en los primeros puestos de ventas, invita a los lectores a adentrarse en un mundo donde lo sobrenatural se entrelaza con la realidad.

